public class variables {
public static void main(String[] args) {
    byte b=127;                //byte numbers of range (-2)^7 to (2^7)-1
    short s=32767;             //short numbers of range (-2)^15 to (2^15)-1
    int i=385647283;           //integers of range (-2)^31 to (2^31)-1
    long l=93848845034232L;          //long numbers of range (-2)^63 to (2^63)-1
    float f=34.23442259f;      // prints upto 4-5 decimal values
    double d=94.59839869485;   //prints uptp 15 decimal values
    char c='B';                //takes single character or letters as input
    boolean bl=true;           //takes true or false as input
    System.out.println(b);
    System.out.println(s);
    System.out.println(i);
    System.out.println(l);
    System.out.println(f);
    System.out.println(d);
    System.out.println(c);
    System.out.println(bl);
}
    
}
