public class DataSizeRange {
    public static void main(String[] args) {
        System.out.println("Maximum size of Integer: "+Integer.MAX_VALUE);
        System.out.println("Minimum size of Integer: "+Integer.MIN_VALUE);
        System.out.println("Integer Bytes: "+Integer.BYTES);
        System.out.println("Maximum size of byte: "+Byte.MAX_VALUE);
        System.out.println("Minimum size of byte: "+Byte.MIN_VALUE);
        System.out.println("Byte byte: "+Byte.BYTES);
        System.out.println("Maximum size of short: "+Short.MAX_VALUE);
        System.out.println("Minimum size of short: "+Short.MIN_VALUE);
        System.out.println("Short byte: "+Short.BYTES);
        System.out.println("Maximum size of Long: "+Long.MAX_VALUE);
        System.out.println("Minimum size of Long: "+Long.MIN_VALUE);
        System.out.println("Long byte: "+Long.BYTES);
        System.out.println("Maximum size of float: "+Float.MAX_VALUE);
        System.out.println("Minimum size of float: "+Float.MIN_VALUE);
        System.out.println("Float byte: "+Float.BYTES);
        System.out.println("Maximum size of double: "+Float.MAX_VALUE);
        System.out.println("Minimum size of double: "+Float.MIN_VALUE);
        System.out.println("Double byte: "+Double.BYTES);


    }
    
}
